set.seed(10)
par(mfrow = c(2, 2))
x <- sort(rnorm(100))
y1 <- 1 + 2 * x + 3 * x^2 + 4 * x^3 + rnorm(100, sd = 1)
y2 <- 1 + 2 * x + 3 * x^2 + 4 * x^3 + rnorm(100, sd = 10)
y3 <- 1 + 2 * x + 3 * x^2 + 4 * x^3 + rnorm(100, sd = 100)
y4 <- 1 + 2 * x + 3 * x^2 + 4 * x^3 + rnorm(100, sd = 1000)

x0 <- seq(-4, 4, len = 1000)
y0 <- 1 + 2 * x0 + 3 * x0^2 + 4 * x0^3
plot(x, y1)
abline(lm(y1~x), col = "blue")
lines(x0, y0, col = "orange", lwd = 3)
lines(x, predict(lm(y1~poly(x, 3))), col="red", main = "SD = 1") 
plot(x, y2)
abline(lm(y2~x), col = "blue")
lines(x0, y0, col = "orange", lwd = 3)
lines(x, predict(lm(y2~poly(x, 3))), col="red", main = "SD = 10") 
plot(x, y3)
abline(lm(y3~x), col = "blue")
lines(x0, y0, col = "orange", lwd = 3)
lines(x, predict(lm(y3~poly(x, 3))), col="red", main = "SD = 100") 
plot(x, y4)
abline(lm(y4~x), col = "blue")
lines(x0, y0, col = "orange", lwd = 3)
lines(x, predict(lm(y4~poly(x, 3))), col="red", main = "SD = 1000") 

